public class temp {
    
    public static void main(String[] args) {
        int x=4,y=6;
        // Bitwise Operation.....
        System.out.println(x&y);
        System.out.println(x|y);
        System.out.println(x^y);

        // Bitwise complement of a number
        System.out.println(~5);
        System.out.println(~6);
        System.out.println(~12);
        System.out.println(~9);
        System.out.println(3&1);
    }    
}
